    public static void fillTable(ResultSet query) throws SQLException{
        String[] data = new String[6];
        
        DefaultTableModel model = (DefaultTableModel) table.getModel();
        model.setRowCount(0);
        
        while(query.next()){
            data[0] = query.getString("id");
            data[1] = query.getString("name");
            data[2] = query.getString("appat");
            data[3] = query.getString("apmat");
            data[4] = query.getString("business");
            data[5] = query.getString("phone");
            model.addRow(data);
        }
        table.setModel(model);
    }
    
