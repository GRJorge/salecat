    private void removeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_removeActionPerformed
        String[] buttons = {"No","Si"};
        int option = JOptionPane.showOptionDialog(null, "Â¿Seguro que quieres eliminar a " + global.getDataTable((DefaultTableModel) table.getModel(), table, 1) + "?","Confirmacion",JOptionPane.DEFAULT_OPTION, JOptionPane.INFORMATION_MESSAGE, null, buttons, buttons[1]);
        
        if(option == 1){
            try{
                providerDB.delete(global.getDataTable((DefaultTableModel) table.getModel(), table, 0));
                fillTable(providerDB.get());
            }catch(SQLException e){
                Logger.getLogger(providerDB.class.getName()).log(Level.SEVERE, null, e);
            }
        }else{
            return;
        }
    }//GEN-LAST:event_removeActionPerformed

